    parser.add_argument("--require-alt-text", action="store_true", help="Fail Google Play assets that have no alt text instead of warning.")
    parser.add_argument("--title", help="Store listing title/app name text to validate.")
    parser.add_argument("--subtitle", help="App Store subtitle text to validate.")
    parser.add_argument("--keywords", help="App Store comma-separated keyword field to validate.")
    parser.add_argument("--short", dest="short_description", help="Google Play short description text to validate.")
    parser.add_argument("--full", dest="full_description", help="Full store description text to validate.")
    parser.add_argument("--promotional-text", help="App Store promotional text to validate.")
    parser.add_argument("--competitor-brand", action="append", default=[], help="Competitor/third-party brand term that must not appear in listing text. Repeatable.")
    parser.add_argument("--locale", help="Listing locale/language code, e.g. en-US or tr-TR.")
    parser.add_argument("--min-count", type=int, help="Required minimum number of assets in the set.")
    parser.add_argument("--max-file-mb", type=float, default=8.0, help="Warn when a file exceeds this size. Default: 8 MB.")
