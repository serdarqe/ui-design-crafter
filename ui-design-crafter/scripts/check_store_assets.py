"""Validate App Store / Google Play screenshot assets before upload.

The script is intentionally dependency-light. It reads PNG/JPEG dimensions from
file headers, understands manifest.store.json generated by export_store_screenshots.py,
and reports store-readiness issues as human text or JSON.
"""

