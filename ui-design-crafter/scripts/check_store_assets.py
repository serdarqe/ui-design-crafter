    parser.add_argument("--preset", help="Store preset name from data/store-screenshot-presets.csv.")
    parser.add_argument("--platform", choices=["app-store", "google-play"], help="Override platform for direct image checks.")
    parser.add_argument("--orientation", choices=["portrait", "landscape", "square", "any"], help="Expected orientation for direct image checks.")
